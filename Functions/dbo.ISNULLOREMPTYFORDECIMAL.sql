CREATE FUNCTION [dbo].[ISNULLOREMPTYFORDECIMAL]
(   
    @value NVARCHAR(max)
)
RETURNS NVARCHAR(MAX)
AS
BEGIN 

IF (@value IS NULL)
BEGIN
    RETURN 0
END
ELSE
BEGIN
    IF (LEN(LTRIM(@value)) = 0)
    BEGIN 
        RETURN 0
    END 
	ELSE
	BEGIN
	   Select @value = ISNULL(TRY_PARSE(@value as decimal(15,2)),0) 

	END
END


RETURN CAST(@value as decimal(18,2));
END
GO
